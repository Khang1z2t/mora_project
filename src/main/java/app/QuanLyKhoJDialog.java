    private void fillToTableS(List<Sach> list) {
        tblModel.setRowCount(0);
        for (Sach s : list) {
            tblModel.addRow(new Object[]{
                s.getMaSach(),
                s.getTenSach(),
            });
        }
    }
    private void fillToTableK(List<Kho> listK) {
        tblModel.setRowCount(0);
        for (Kho s : listK) {
            tblModel.addRow(new Object[]{
                s.getMaSach(),
                s.getTenSach(),
            });
        }
    }
